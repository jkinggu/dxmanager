package com.dx.dxmanage.po;

public class RolePermission {
	private Long rid;

	private Long pid;

	public Long getRid() {
		return rid;
	}

	public void setRid(Long rid) {
		this.rid = rid;
	}

	public Long getPid() {
		return pid;
	}

	public void setPid(Long pid) {
		this.pid = pid;
	}

	public RolePermission() {
		super();
	}

	public RolePermission(Long rid, Long pid) {
		super();
		this.rid = rid;
		this.pid = pid;
	}

}