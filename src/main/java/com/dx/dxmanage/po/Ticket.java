package com.dx.dxmanage.po;

public class Ticket {
    private Integer ticketid;
    
    private String tickettime;

    private String beginnum;

    private Integer ticketamount;

    private String endnum;
    
    private String usenum;

    public Integer getTicketid() {
        return ticketid;
    }

    public void setTicketid(Integer ticketid) {
        this.ticketid = ticketid;
    }
    
    public String getTickettime() {
		return tickettime;
	}

	public void setTickettime(String tickettime) {
		this.tickettime = tickettime;
	}

	public String getBeginnum() {
        return beginnum;
    }

    public void setBeginnum(String beginnum) {
        this.beginnum = beginnum;
    }

    public Integer getTicketamount() {
        return ticketamount;
    }

    public void setTicketamount(Integer ticketamount) {
        this.ticketamount = ticketamount;
    }

    public String getEndnum() {
        return endnum;
    }

    public void setEndnum(String endnum) {
        this.endnum = endnum;
    }

	public String getUsenum() {
		return usenum;
	}

	public void setUsenum(String usenum) {
		this.usenum = usenum;
	}

	public Ticket(Integer ticketid, String tickettime, String beginnum, Integer ticketamount, String endnum,
			String usenum) {
		super();
		this.ticketid = ticketid;
		this.tickettime = tickettime;
		this.beginnum = beginnum;
		this.ticketamount = ticketamount;
		this.endnum = endnum;
		this.usenum = usenum;
	}

	public Ticket() {
		super();
	}

	@Override
	public String toString() {
		return "Ticket [ticketid=" + ticketid + ", tickettime=" + tickettime + ", beginnum=" + beginnum
				+ ", ticketamount=" + ticketamount + ", endnum=" + endnum + ", usenum=" + usenum + "]";
	}
    
    
}